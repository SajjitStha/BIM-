class methodoverloading
{
    void show()
    {
        System.out.println("This is a function");
    }
    void show(int a)
    {
        System.out.println("This is a function with parameter of int");
    }
    void show(double a)
    {
        System.out.println("This is a function with parameter of double");
    }
    void show(int a,int b )
    {
        System.out.println("This is a function with 2 integer");

    }
    
}
